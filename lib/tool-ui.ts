export type ToolUi = (typeof toolUi)[Locale];
export type CssMode = keyof ToolUi["cssModes"];
export type ToolUnit = "px" | "in" | "cm" | "mm" | "ft";

const unitMap: Record<Exclude<ToolUnit, "px">, Intl.NumberFormatOptions["unit"]> = {
  in: "inch",
  cm: "centimeter",
  mm: "millimeter",
  ft: "foot",
};

export function getToolUi(locale: Locale): ToolUi {
  return toolUi[locale] || toolUi.en;
}

export function getCssModeConfig(locale: Locale, mode: CssMode) {
  return getToolUi(locale).cssModes[mode];
}

export function getUnitLabel(locale: Locale, unit: ToolUnit): string {
  if (unit === "px") {
    return getToolUi(locale).common.pixels;
  }

  const parts = new Intl.NumberFormat(locale, {
    style: "unit",
    unit: unitMap[unit],
    unitDisplay: "long",
  }).formatToParts(2);

  return parts.find((part) => part.type === "unit")?.value || unit;
}
